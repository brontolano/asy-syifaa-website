<script>
/* ERP_WEBSITE_INTEGRATION_V1 */
(function(){
  const main=document.querySelector('main.container');
  if(!main) return;
  const params = new URLSearchParams(window.location.search);
  const ticketQuery = (params.get('ticket') || '').trim();
  main.innerHTML = `
    <div class="row justify-content-center">
      <div class="col-lg-12">
        <section class="hs-shell" style="max-width:1100px; width:100%;">
          <div class="hs-top">
            <h3 class="hs-title">Hasil Seleksi PPDB</h3>
            <span id="ppdbCount" class="badge text-bg-success">Memuat...</span>
          </div>
          <p class="mb-2 text-muted">Data pendaftaran dari website terhubung ke ERP dan diperbarui real-time.</p>
          <form id="ticketForm" class="hs-filter">
            <input id="ticketInput" type="text" class="form-control" placeholder="Masukkan E-Ticket (contoh: ETK-20260519-000006)" value="${ticketQuery.replace(/"/g, '&quot;')}" />
            <button type="submit" class="btn btn-success"><i class="bi bi-search me-1"></i> Cek</button>
          </form>
          <p id="ticketNotice" class="small mb-3 text-muted"></p>
          <div class="table-responsive hs-table-wrap">
            <table class="table table-striped align-middle mb-0 hs-table">
              <thead><tr><th>No. Pendaftaran</th><th>Nama Calon Santri</th><th>Asal Sekolah</th><th>Nama Wali</th><th>Tahun Ajaran</th><th>Status</th></tr></thead>
              <tbody id="ppdbResultsBody"><tr><td colspan="6" class="text-center text-muted">Memuat data...</td></tr></tbody>
            </table>
          </div>
          <div class="hs-actions">
            <a href="/daftar-sekarang" class="btn load-more-btn">Daftar PPDB Baru</a>
            <a href="/hasil-seleksi" class="btn btn-outline-success">Reset Filter</a>
          </div>
        </section>
      </div>
    </div>`;

  async function getJson(path){
    const url = (window.ASF_API_BASE || "") + path;
    const res = await fetch(url);
    if(!res.ok) throw new Error('erp_fetch_failed');
    return await res.json();
  }

  function statusLabel(status){
    const key = String(status || '').toLowerCase();
    if(key === 'accepted' || key === 'lulus') return 'Lulus / Diterima';
    if(key === 'waiting_list' || key === 'cadangan') return 'Cadangan';
    if(key === 'rejected') return 'Tidak Lulus';
    return 'Menunggu Seleksi';
  }
  function statusClass(status){
    const key = String(status || '').toLowerCase();
    if(key === 'accepted' || key === 'lulus') return 'verified';
    if(key === 'rejected') return 'rejected';
    return 'pending';
  }

  const endpoint = ticketQuery
    ? `/api/v1/ppdb/selection-results?search=${encodeURIComponent(ticketQuery)}`
    : '/api/v1/ppdb/selection-results';

  getJson(endpoint)
    .then(({ok,data})=>{
      const body=document.getElementById('ppdbResultsBody');
      const count=document.getElementById('ppdbCount');
      const ticketNotice=document.getElementById('ticketNotice');
      if (ticketNotice) {
        ticketNotice.textContent = ticketQuery
          ? `Filter aktif untuk E-Ticket: ${ticketQuery}`
          : 'Gunakan E-Ticket dari halaman pendaftaran untuk cek hasil seleksi.';
      }
      if(!ok || !Array.isArray(data) || data.length===0){
        body.innerHTML='<tr><td colspan="6" class="text-center text-muted">Belum ada data PPDB.</td></tr>';
        count.textContent='0 data';
        return;
      }
      count.textContent=`${data.length} data`;
      body.innerHTML=data.map(row=>{
        const ticket = row.registration_number || row.eticket_no || '-';
        const status = statusLabel(row.status);
        const statusCls = statusClass(row.status);
        const highlight = ticketQuery && ticket.toLowerCase() === ticketQuery.toLowerCase() ? 'table-success' : '';
        return `<tr class="${highlight}"><td><strong>${ticket}</strong></td><td>${row.student_name||'-'}</td><td>${row.origin_school||'-'}</td><td>${row.guardian_name||'-'}</td><td>${row.academic_year||'-'}</td><td><span class="hs-status ${statusCls}">${status}</span></td></tr>`;
      }).join('');
    })
    .catch(()=>{
      const body=document.getElementById('ppdbResultsBody');
      body.innerHTML='<tr><td colspan="6" class="text-center text-danger">Gagal memuat data PPDB.</td></tr>';
    });

  const ticketForm = document.getElementById('ticketForm');
  const ticketInput = document.getElementById('ticketInput');
  if (ticketForm && ticketInput) {
    ticketForm.addEventListener('submit', function(event){
      event.preventDefault();
      const value = ticketInput.value.trim();
      const url = value ? `/hasil-seleksi?ticket=${encodeURIComponent(value)}` : '/hasil-seleksi';
      window.location.href = url;
    });
  }
})();
</script>
